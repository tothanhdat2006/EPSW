									<Loader2 size={18} class="animate-spin" /> {ui.processing}
								{:else if decision === 'APPROVED'}
									<CheckCircle2 size={18} /> {ui.approveNotify}
								{:else if decision === 'REJECTED'}
									<XCircle size={18} /> {ui.rejectNotify}
								{:else}
									<AlertCircle size={18} /> {ui.revisionNotify}
								{/if}
							</button>

							{#if decision === 'APPROVED'}
								<p
									class="flex items-center justify-center gap-1.5 text-center text-[11px] text-muted-foreground"
