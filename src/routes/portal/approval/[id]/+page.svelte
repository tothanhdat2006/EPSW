		class="flex w-full flex-col overflow-hidden border-r border-border/40 bg-background md:w-1/2"
	>
		<!-- Header -->
		<div class="border-b border-border/30 bg-muted/10 p-6">
			<div class="mb-4 flex items-center gap-2">
