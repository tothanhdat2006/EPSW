					revise: 'Sửa đổi',
					reviseHint: 'Yêu cầu bổ sung nội dung',
					reject: 'Từ chối',
					rejectHint: 'Không đạt yêu cầu',
					directiveRequired: 'Ý kiến chỉ đạo (bắt buộc)',
					directiveOptional: 'Ý kiến chỉ đạo (tùy chọn)',
					drafting: 'Đang soạn...',
					aiDraft: 'AI Soạn thảo',
