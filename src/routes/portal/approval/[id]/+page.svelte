						</div>
					{:else if submitted || doc.status === 'APPROVED' || doc.status === 'REJECTED' || doc.status === 'REVISION_REQUESTED'}
						<!-- Post-decision state -->
						{@const isApproved = doc.status === 'APPROVED'}
						{@const isRejected = doc.status === 'REJECTED'}
						<div
							class="flex items-center gap-4 rounded-2xl border p-6
							{isApproved
								? 'border-emerald-500/20 bg-emerald-500/10 text-emerald-600'
								: isRejected
									? 'border-destructive/20 bg-destructive/10 text-destructive'
									: 'border-amber-500/20 bg-amber-500/10 text-amber-600'}"
						>
							{#if isApproved}
								<CheckCircle2 size={28} />
								<div>
									<p class="font-extrabold">{ui.approvedResult}</p>
									<p class="mt-0.5 flex items-center gap-1 text-xs font-medium opacity-80">
										<Mail size={11} />
										{ui.approvedMailSent}
									</p>
									<a
										href="/portal"
										class="mt-2 block text-xs font-semibold underline underline-offset-4 opacity-70 hover:opacity-100"
										>{ui.backMissionControl}</a
									>
								</div>
							{:else if isRejected}
								<XCircle size={28} />
								<div>
									<p class="font-extrabold">{ui.rejectedResult}</p>
									<p class="mt-0.5 flex items-center gap-1 text-xs font-medium opacity-80">
										<Mail size={11} />
										{ui.rejectedMailSent}
									</p>
									<a
										href="/portal"
										class="mt-2 block text-xs font-semibold underline underline-offset-4 opacity-70 hover:opacity-100"
										>{ui.backMissionControl}</a
									>
								</div>
							{:else}
								<AlertCircle size={28} />
								<div>
									<p class="font-extrabold">{ui.revisionResult}</p>
									<p class="mt-0.5 text-xs font-medium opacity-80">
										{ui.revisionStatusHint}
									</p>
									<a
										href="/portal"
										class="mt-2 block text-xs font-semibold underline underline-offset-4 opacity-70 hover:opacity-100"
										>{ui.backMissionControl}</a
									>
								</div>
							{/if}
						</div>
					{:else}
						<!-- Not in PENDING_APPROVAL state -->
						<div
							class="flex items-center gap-3 rounded-2xl border border-border/50 bg-muted/10 p-5 font-bold text-muted-foreground"
						>
							<CheckCircle2 size={24} />
							<div>
								{ui.notReady}
								<a
									href="/portal"
									class="mt-1 block text-xs font-semibold underline decoration-border underline-offset-4"
									>{ui.backMissionControl}</a
								>
							</div>
						</div>
					{/if}
				</div>
			</div>
		{:else if isLoading}
			<div class="flex flex-1 items-center justify-center">
				<Loader2 size={32} class="animate-spin text-primary" />
			</div>
		{:else if errorMsg}
			<div class="flex flex-1 items-center justify-center p-8">
				<p class="font-bold text-destructive">{errorMsg}</p>
			</div>
		{/if}
	</div>
</div>
