	<title>{ui.title} — DVC Portal</title>
</svelte:head>

<div
	class="flex h-full animate-in flex-col overflow-hidden bg-muted/10 duration-500 fade-in md:flex-row"
>
	<!-- Left Panel: Document viewer -->
