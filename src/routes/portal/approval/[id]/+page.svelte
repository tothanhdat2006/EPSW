										class="sr-only"
									/>
									<div
										class="flex flex-col items-center justify-center gap-2 rounded-2xl border p-5 text-center transition-all
										{decision === 'REVISION_REQUESTED'
											? 'border-amber-500 bg-amber-500/10 text-amber-600 ring-1 ring-amber-500/50'
											: 'border-border/60 text-muted-foreground hover:bg-muted/30'}"
									>
										<AlertCircle size={26} />
										<span class="text-xs leading-tight font-extrabold">{ui.revise}</span>
										<span class="text-[10px] opacity-70">{ui.reviseHint}</span>
									</div>
								</label>
								<label class="cursor-pointer">
									<input
										type="radio"
										name="decision"
										value="REJECTED"
										bind:group={decision}
										class="sr-only"
									/>
									<div
										class="flex flex-col items-center justify-center gap-2 rounded-2xl border p-5 text-center transition-all
										{decision === 'REJECTED'
											? 'border-destructive bg-destructive/10 text-destructive ring-1 ring-destructive/50'
											: 'border-border/60 text-muted-foreground hover:bg-muted/30'}"
									>
										<XCircle size={26} />
										<span class="text-xs leading-tight font-extrabold">{ui.reject}</span>
										<span class="text-[10px] opacity-70">{ui.rejectHint}</span>
									</div>
								</label>
							</div>

							<!-- Remarks -->
							<div class="space-y-2">
								<div class="flex items-center justify-between gap-3">
									<label
										for="ld-feedback"
										class="text-[11px] font-black tracking-widest text-muted-foreground uppercase"
									>
										{decision === 'REJECTED' || decision === 'REVISION_REQUESTED'
											? ui.directiveRequired
											: ui.directiveOptional}
									</label>
									<button
										onclick={generateAILeaderBrief}
										disabled={generatingLeaderBrief}
										class="flex shrink-0 items-center gap-2 rounded-lg bg-primary/10 px-3 py-1.5 text-xs font-bold text-primary transition-colors hover:bg-primary/20 disabled:opacity-50"
									>
										{#if generatingLeaderBrief}
											<Loader2 size={14} class="animate-spin" /> {ui.drafting}
