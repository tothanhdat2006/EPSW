					officerBrief: 'Tóm tắt báo cáo Chuyên viên (AI)',
					officerBriefFallback: '(Tóm tắt tài liệu — báo cáo Chuyên viên đang được AI xử lý)',
					noOfficerBrief: 'Chưa có báo cáo tổng hợp.',
					reviewMinutes: 'Biên bản xử lý chuyên môn',
					officerRole: 'Chuyên viên',
					recommendApprove: 'Đề nghị Phê duyệt',
					requestRevision: 'Yêu cầu Sửa đổi',
					emptyMinutes: '(Không có nội dung biên bản)',
