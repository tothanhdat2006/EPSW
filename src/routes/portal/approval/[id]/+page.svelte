									>
								</div>
								<div class="flex items-center gap-2">
									{#if fb.decision === 'APPROVE'}
										<span
											class="flex items-center gap-1 rounded-full border border-emerald-500/20 bg-emerald-500/10 px-2 py-0.5 text-[10px] font-bold text-emerald-600"
