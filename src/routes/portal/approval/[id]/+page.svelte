					leaderDecision: 'Quyết định của Lãnh đạo',
					leaderDecisionHint:
						'Kết quả sẽ được thông báo đến công dân qua email ngay sau khi Lãnh đạo chốt quyết định.',
					approve: 'Phê duyệt',
