					aiDraft: 'AI Draft',
					approvePlaceholder: 'Approve according to the officer recommendation...',
					aiDraftPlaceholder: 'Press "AI Draft" to fill in the directive automatically...',
					processing: 'Processing...',
					approveNotify: 'Approve & Notify Citizen',
					rejectNotify: 'Reject & Send Email Notification',
					revisionNotify: 'Request Record Revision',
					approveMailHint:
						'The system will automatically send the approval confirmation email to the citizen.',
					rejectMailHint:
						'The system will automatically send the rejection email with the reason to the citizen.',
					revisionHint: 'The record will be returned to the officer for revision.',
					approvedResult: 'The record has been approved',
					approvedMailSent: 'Confirmation email has been sent to the citizen.',
					rejectedResult: 'The record has been rejected',
					rejectedMailSent: 'Notification email has been sent to the citizen.',
					revisionResult: 'The record requires revision',
					revisionStatusHint: 'The record status has been moved back to the officer.',
					backMissionControl: 'Back to Mission Control',
					notReady: 'The record has not been submitted by the officer yet.'
				}
			: {
					title: 'Phê duyệt hồ sơ',
					back: 'Quay lại',
					badge: 'Phê duyệt cấp Lãnh đạo',
					originalRecord: 'Hồ sơ gốc',
